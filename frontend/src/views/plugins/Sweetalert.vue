<script lang="ts">
import { defineComponent } from 'vue'
import Swal from 'sweetalert2'
import IMAGE from '@/constent/Theme'
import CommonNavbar from '@/layouts/CommonNavbar.vue'

export default defineComponent({
  name: 'sweetalerts_',
  components: { CommonNavbar },
  setup() {
    return {}
  },
  methods: {
    showAlert(value: string) {
      switch (value) {
        case 'wrong':
          Swal.fire({
            icon: 'error',
            title: 'Oops...',
            text: 'Something went wrong !!'
          })
          break
        case 'message':
          Swal.fire('Hey, Here s a message !!')
          break
        case 'text':
          Swal.fire({
            title: 'Hey, Here s a message !!',
            text: 'It s pretty, isn t it?'
          })
          break
        case 'success':
          Swal.fire({
            icon: 'success',
            title: 'Success Alert',
            text: 'This is a success alert.'
          })
          break
        case 'confirm':
          Swal.fire({
            icon: 'warning',
            title: 'Are you sure to delete ?',
            text: 'You will not be able to recover this imaginary file !!',
            confirmButtonText: 'Yes, delete it!',
            cancelButtonText: 'No, cancel!',
            showCancelButton: true
          })
          break
        case 'cancel':
          Swal.fire({
            icon: 'warning',
            title: 'Are you sure to delete ?',
            text: 'You will not be able to recover this imaginary file !!',
            confirmButtonText: 'Yes, delete it!',
            cancelButtonText: 'No, cancel!',
            showCancelButton: true
          })
          break
        case 'image-message':
          Swal.fire({
            imageUrl: IMAGE.imageHand,
            title: 'Sweet !!',
            text: 'true'
          })
          break
        case 'html':
          Swal.fire({
            title: 'Sweet !!',
            text: 'true'
          })
          break
        case 'auto':
          Swal.fire({
            title: 'Sweet auto close alert !!',
            text: 'Hey, i will close in 2 seconds !!'
          })
          setTimeout(() => {
            Swal.close()
          }, 2000)
          break

        case 'prompt':
          Swal.fire({
            title: 'Enter an input !!',
            text: 'Write something interesting !!',
            confirmButtonText: 'Yes, delete it!',
            cancelButtonText: 'No, cancel!',
            showCancelButton: true
          })
          break
        case 'ajax':
          Swal.fire({
            icon: 'warning',
            title: 'Sweet ajax request !!',
            text: 'Submit to run ajax request !!',
            confirmButtonText: 'Ok',
            cancelButtonText: 'Cancel',
            showCancelButton: true
          })
          break
        default:
          break
      }
    }
  }
})
